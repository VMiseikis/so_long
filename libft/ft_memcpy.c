#include <stdio.h>

void	*ft_memcpy(void *dest, const void *src, int n)
{
	int			i;
	const char	*s;
	char		*d;

	i = 0;
	if (!src && !dest)
		return (NULL);
	s = (char *)src;
	d = (char *)dest;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
